    <p>Nasi goreng merupakan sajian nasi yang digoreng dalam sebuah wajan atau penggorengan menghasilkan cita rasa berbeda karena dicampur dengan bumbu-bumbu seperti bawang putih, bawang merah, merica dan kecap manis. Selain itu, ditambahkan bahan-bahan pelengkap seperti telur, sayur-sayuran, makanan laut, atau daging. Makanan tersebut sering kali disantap sendiri atau disertai dengan hidangan lainnya. Nasi goreng adalah komponen populer dari masakan Asia Timur, Tenggara dan Selatan pada wilayah tertentu. Sebagai hidangan buatan rumah, nasi goreng biasanya dibuat dengan bahan-bahan yang tersisa dari hidangan lainnya, yang berujung pada ragam yang tak terbatas. Menjadi penopang ekonomi, hal yang sama juga terjadi pada mi goreng atau pyttipanna.</p>
    <p>Beberapa ragam populer dari nasi goreng memiliki daftar bahan spesifik mereka sendiri. Di Tiongkok Raya, ragam paling terkenal meliputi nasi goreng Yangzhou dan nasi goreng Hokkien. ChÄhan Jepang dianggap sebuah hidangan Tionghoa Jepang, yang berasal dari hidangan nasi goreng Tionghoa. Bokkeum-bap dari Korea umumnya tak dianggap demikian, meskipun terdapat ragam Tionghoa Korea dari bokkeum-bap. Di Asia Tenggara, nasi goreng Indonesia, Malaysia, dan Singapura yang berkonstruksi serupa dan khao phat Thai adalah hidangan populer. Di Barat, kebanyakan restoran untuk pelanggan vegetarian atau Muslim membuat ragam mereka sendiri dari nasi goreng, termasuk nasi goreng telur. Nasi goreng juga muncul pada menu-menu restoran Amerika yang menawarkan masakan dengan tanpa tradisi asli dari hidangan tersebut. Selain itu, terdapat ragam nasi goreng di negara-negara selatan Amerika Serikat. Beberapa ragam tersebut meliputi chaulafan asal Ekuador, arroz chaufa asal Peru, arroz frito asal Kuba, dan arroz mamposteao asal Puerto Rico.</p>
    <p>Nasi goreng adalah makanan jalanan populer di Asia. Di beberapa negara Asia, restoran-restoran kecil, gerai-gerai pinggir jalan dan pedagang keliling mengkhususkan diri dalam menyajikan nasi goreng. Di kota-kota Indonesia, pedagang umum nasi goreng biasa ditemukan berpindah-pindah dengan gerobak makanan mereka dan berhenti di jalanan sibuk atau kawasan tempat tinggal. Beberapa gerai makanan Asia Tenggara menawarkan nasi goreng dengan pilihan rasa dan hidangan sampingan.</p>

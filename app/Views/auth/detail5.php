    <p>Martabak (bahasa Arab: مطبق, berarti "terlipat") merupakan sajian yang biasa ditemukan di Arab Saudi (terutama di wilayah Hijaz), Yaman, Indonesia, Malaysia, Singapura, dan Brunei. Bergantung pada lokasinya, nama, dan komposisi martabak dapat bervariasi.</p>
    <p>Di Indonesia ada dua jenis martabak, yaitu martabak asin/telur yang terbuat dari campuran telur dan daging serta martabak manis, padahal yang benar(kue terang bulan) yang biasanya di isi coklat dan keju. Berbeda dengan martabak telur, martabak manis adalah sejenis kue atau roti isi selai yang biasa dimakan di saat santai sebagai makanan ringan. Di Malaysia, martabak manis (yang dikenal dengan nama Apam Balik) sering dijadikan sebagai hidangan sarapan dengan ditemani segelas teh tarik.</p>
